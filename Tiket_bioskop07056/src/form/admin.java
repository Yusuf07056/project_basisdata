    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here
        DefaultTableModel model = (DefaultTableModel) tablefilm.getModel();
        Integer kode = tablefilm.getSelectedRow();
        kode_film = tablefilm.getValueAt(kode, 0).toString();
        kodefilm.setText(kode_film);
        
        try {
                this.transaksi.delete_film(kode_film);
                this.showtablefilm();
        } catch (SQLException ex) {
  
            System.out.println(ex);
            Logger.getLogger(admin.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null,"salah bro coba di cek lagi");
        }
        
    }//GEN-LAST:event_delActionPerformed

