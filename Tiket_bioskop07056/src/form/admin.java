    public void showtablefilm()throws SQLException{
        DefaultTableModel tblfilm = new DefaultTableModel(new String[]{
            "kode film", "nama film", "genre", "rating", "harga"
        },0);
        tblfilm.setRowCount(0);
        for (film fl : this.transaksi.get_film()) {
            tblfilm.addRow(new String[]{
                fl.gekodfilm(),fl.genamafilm(),fl.gegenre(),fl.gerating(),fl.get_harga().toString()
            });
            this.tablefilm.setModel(tblfilm);
        }
    }
